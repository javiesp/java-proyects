    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // Calcula el promedio 
        int nota1 = Integer.parseInt(txNota1.getText());//Notas 
        int nota2 = Integer.parseInt(txNota2.getText());
        int nota3 = Integer.parseInt(txNota3.getText());
        int pon1 =  Integer.parseInt(txPon1.getText());//Porcetaje de ponderacion 
        int pon2 =  Integer.parseInt(txPon2.getText());
        int pon3 =  Integer.parseInt(txPon3.getText());
        
        int porc1 = (nota1*pon1/100);//Calculo de cada porcentaje
        int porc2 = (nota2*pon2/100);
        int porc3 = (nota3*pon3/100);
     
        if (pon1+pon2+pon3 > 100 ) {
  
            lbPromedio.setText("La ponderaci√≥n debe ser Igual a 100");
        }else{  
            int PromedioFinal = (porc1+porc2+porc3);
            lbPromedio.setText(String.valueOf(PromedioFinal));
        }

    }//GEN-LAST:event_btnCalcActionPerformed

